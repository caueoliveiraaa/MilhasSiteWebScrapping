
def print_yellow(message):
    print(
        '\033[033m' +
        message +
        '\033[0m'
    )


def print_green(message):
    print(
        '\033[032m' +
        message +
        '\033[0m'
    )


def print_red(message):
    print(
        '\033[031m' +
        message +
        '\033[0m'
    )


def print_cyan(message):
    print(
        '\033[036m' +
        message +
        '\033[0m'
    )


def print_purple(message):
    print(
        '\033[035m' +
        message +
        '\033[0m'
    )